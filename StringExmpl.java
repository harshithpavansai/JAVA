class StringExmpl {
	public static void main(String[] args) {
		String name = "javaa is the goat programming language";
		System.out.println(name.charAt(1));
		System.out.println(name.charAt(4));
		String a = "goat";
		System.out.println(name.contains(a));
		System.out.println(name.contains("mmi"));
		System.out.println(name.contains("jaa"));
	}
}