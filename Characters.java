public class Characters {
    public static void main(String[] args) {
        char ch1 = 'a';
        char ch2 = 'b';
        if (ch1 > ch2)
            System.out.println("YES");
        else
            System.out.println("NO");
        System.out.println((int) ch1);

        int x = 3077;
        System.out.println((char) x);
    }
}
